    private void Refrescar () {
        DefaultComboBoxModel cmbModel = new DefaultComboBoxModel();
        
        for (AndroControl android : Acc.getAndroides(4))
            cmbModel.addElement(android);
        jComboBox1.setModel(cmbModel);
        
        cmbModel = new DefaultComboBoxModel();
        
        for (AccionControl action: Acc.getActions())
            cmbModel.addElement(action);
        jComboBox2.setModel(cmbModel);
        
        
        DefaultListModel lstModel = new DefaultListModel();
        
        for (AndroControl android : Acc.getAndroides(1))
            lstModel.addElement(android);
        lstTodos.setModel(lstModel);
        
        lstModel = new DefaultListModel();
        
        for (AndroControl android : Acc.getAndroides(2))
            lstModel.addElement(android);
        lstMenos.setModel(lstModel);
        
        lstModel = new DefaultListModel();
        
        for (AndroControl android : Acc.getAndroides(3))
            lstModel.addElement(android);
        lstVida.setModel(lstModel);
        
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         try {
            String name = txtNombreAc.getText().trim();
            int energy = Integer.parseInt(txtEner.getText().trim());
            
            if (!name.equals("")) 
                Acc.AgregarAccion(name, energy);
            else
                JOptionPane.showMessageDialog(this, "Ingrese el nombre de la acci贸n.");
            Refrescar();
            
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La energia debe ser un valor entero.");
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri贸 un error al capturar los datos.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

